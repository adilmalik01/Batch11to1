


let first_number = parseInt( prompt("Enter your first number :") )
let operator = prompt("Enter your Operator :")
let second_number = +prompt("Enter your second number :")

if (operator == "+") {
    alert(first_number + second_number)
}else if(operator == "-"){
   alert(first_number - second_number)
}else if(operator == "*"){
    alert(first_number * second_number)
}else{
    alert("Invalid Operator")
}

















